<p>El Jardinet és una caseta ubicada a Borredà; un petit però marevellós municipi de muntanya situat a la comarca del Berguedà. 
La casa disposa d'un jardí, té vistes a la muntanya i es troba a 40 km de Ribes de Freser.</p>

<p> Aquesta casa de 3 habitacions inclou un bany, TV de pantalla plana amb canals per cable, zona de menjador, cuina totalment equipada i balcó amb vistes al jardí.
    
A la casa també hi ha una terrassa. </p>
    
<p> Es troba a 50 km de Camprodon i a 47 km de Manresa. L'aeroport més proper és el de Girona-Costa Brava, ubicat a 114 km.</p>

